using System;
using System.Collections.Generic;
using System.Drawing;
using System.Linq;
using System.Text;
using System.Threading.Tasks;


namespace experimental_hospital_management_system
{
    public class MoreColors
    {
        public MoreColors() { }
        public Color RoyalBlue { get { return Color.FromArgb(67, 132, 222); } }
        public Color SteelBlue { get { return Color.FromArgb(68, 114, 196); } }
        public Color MediumSeaGreen { get { return Color.FromArgb(67, 222, 132); } }
        public Color Crimson { get { return Color.FromArgb(222, 67, 67); } }
        public Color Lavender { get { return Color.FromArgb(235, 237, 239); } }
        public Color White { get { return Color.White; } }
        public Color DarkSlateGray { get { return Color.FromArgb(35, 38, 42); } }
        public Color SlateGray { get { return Color.FromArgb(116, 127, 141); } }
        
    }
}
